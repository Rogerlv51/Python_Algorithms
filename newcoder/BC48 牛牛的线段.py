# 问题描述
# 牛牛有一个线段放在二维坐标系中，已知线段两个点的坐标 (x1,y1)，(x2,y2) 。牛牛想知道这个线段的长度的平方

# 输入描述
# 第一行输入 x1 和 y1，用空格隔开。
# 第二行输入 x2 和 y2，用空格隔开。
# 其中 x1 ， y1 ，x2 ，y2 都是整数

x1, y1 = input().split(" ")     
# 注意转型，input默认返回字符串类型
x1, y1 = int(x1), int(y1)
x2, y2 = input().split(" ")
x2, y2 = int(x2), int(y2)
print(pow(x1-x2, 2) + pow(y1-y2, 2))   # 平方运算